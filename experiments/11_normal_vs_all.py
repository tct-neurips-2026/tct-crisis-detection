all_results = {}

for neg_label, pos_label in CONTRASTS:
    contrast_name = f"N vs {pos_label[:3]}"
    print(f"\n{'='*60}")
    print(f"Contrast: Normal vs. {pos_label}")
    print(f"{'='*60}")

    # 세션 필터링
    contrast_sessions = []
    for s in session_data:
        if s['crisis'] in [neg_label, pos_label]:
            s2 = dict(s)
            s2['crisis_label'] = (
                1 if s['crisis'] == pos_label
                else 0)
            contrast_sessions.append(s2)

    n_neg = sum(1 for s in contrast_sessions
                if s['crisis_label'] == 0)
    n_pos = sum(1 for s in contrast_sessions
                if s['crisis_label'] == 1)
    print(f"  {neg_label}: {n_neg}, "
          f"{pos_label}: {n_pos}, "
          f"Total: {len(contrast_sessions)}")

    if len(contrast_sessions) < 100:
        print(f"  ⚠️ Too few, skipping.")
        continue

    train_c, test_c = train_test_split(
        contrast_sessions,
        test_size=0.2,
        random_state=42,
        stratify=[s['crisis_label']
                  for s in contrast_sessions]
    )

    results = {
        'TCT-GRU':  {T: [] for T in T_LIST},
        'Full-GRU': {T: [] for T in T_LIST},
    }

    for seed in SEEDS:
        print(f"  Seed {seed}...")

        m_tct, auc = run_experiment(
            train_c, test_c,
            dataset_cls=TCTDataset,
            seed=seed, epochs=25)
        print(f"    TCT: {auc:.3f}")

        m_full, auc = run_experiment(
            train_c, test_c,
            dataset_cls=FullDataset,
            seed=seed, epochs=25)
        print(f"    Full: {auc:.3f}")

        for T in T_LIST:
            results['TCT-GRU'][T].append(
                eval_at_T(m_tct, test_c, T))
            results['Full-GRU'][T].append(
                eval_at_T(m_full, test_c, T))

    all_results[f"N_vs_{pos_label}"] = results

