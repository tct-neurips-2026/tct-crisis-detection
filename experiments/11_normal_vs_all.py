def eval_at_T(model, sessions, T):
    scores, labels = [], []
    model.eval()
    with torch.no_grad():
        for sess in sessions:
            t = min(T, len(sess['embeddings']))
            embs = torch.FloatTensor(
                sess['embeddings'][:t]
            ).unsqueeze(0).to(device)
            length = torch.LongTensor([t])
            logit  = model(embs, length)
            scores.append(
                torch.sigmoid(logit).item())
            labels.append(sess['crisis_label'])
    return roc_auc_score(labels, scores)


def run_experiment(train_sessions,
                   test_sessions,
                   dataset_cls,
                   seed=42, epochs=25,
                   **ds_kwargs):
    torch.manual_seed(seed)
    np.random.seed(seed)

    ds     = dataset_cls(
        train_sessions, **ds_kwargs)
    loader = DataLoader(
        ds, batch_size=32,
        shuffle=True,
        collate_fn=collate_fn)

    model     = GRUClassifier().to(device)
    optimizer = torch.optim.Adam(
        model.parameters(), lr=1e-3)
    criterion = nn.BCEWithLogitsLoss()
    best_auc  = 0
    best_state = {
        k: v.clone()
        for k, v in model.state_dict().items()}

    for epoch in range(1, epochs + 1):
        model.train()
        for embs, labels, lengths in loader:
            embs    = embs.to(device)
            labels  = labels.to(device)
            lengths = lengths.to(device)
            optimizer.zero_grad()
            logit = model(embs, lengths)
            loss  = criterion(logit, labels)
            loss.backward()
            nn.utils.clip_grad_norm_(
                model.parameters(), 1.0)
            optimizer.step()

        try:
            model.eval()
            preds, labs = [], []
            with torch.no_grad():
                for sess in test_sessions:
                    embs = torch.FloatTensor(
                        sess['embeddings']
                    ).unsqueeze(0).to(device)
                    length = torch.LongTensor(
                        [len(
                            sess['embeddings'])])
                    logit  = model(embs, length)
                    preds.append(
                        torch.sigmoid(
                            logit).item())
                    labs.append(
                        sess['crisis_label'])
            auc = roc_auc_score(labs, preds)
            if auc > best_auc:
                best_auc   = auc
                best_state = {
                    k: v.clone()
                    for k, v in
                    model.state_dict().items()}
        except Exception as e:
            print(f"    Epoch {epoch} "
                  f"AUC error: {e}")
            continue

    model.load_state_dict(best_state)
    return model, best_auc

